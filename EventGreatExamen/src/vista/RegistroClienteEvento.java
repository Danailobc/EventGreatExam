    private void jButtonAgregarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarClienteActionPerformed

        try {
            Cliente cliente = new Cliente(txtRut.getText(), txtNombreCompleto.getText(), txtCorreo.getText(), Integer.parseInt(txtTelefono.getText()));
            if (cliente.validarLargoRut()) {
                if (cliente.validarLargoTelefono()) {
                    col.agregarRegistroCliente(cliente, jTextArea1);
                } else {
                    jTextArea1.setText("Debes agregar 9 numeros para agregar el telefono");
                }
            } else {
                jTextArea1.setText("El RUT no es valido debes agregar entre 9 a 10 caracteres.");
            }
        } catch (NumberFormatException e) {
            jTextArea1.setText("El campo de telefono debe ser un valor numerico.");
        }
        
        listarDatosClientes();
    }//GEN-LAST:event_jButtonAgregarClienteActionPerformed

