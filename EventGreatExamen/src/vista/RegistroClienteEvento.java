    private void jButtonAgregarEventoSolicitadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarEventoSolicitadoActionPerformed
        // TODO add your handling code here:
        String rutBuscado = txtClienteEventoSolicitado.getText();
        Cliente cliente = new Cliente();
        for(Cliente c: col.listarClientes()) {
            if (c.getRut().equals(rutBuscado)) {
                cliente = c;
            }
        }
       
        boolean isMatrimonio = false;
        int id_evento_Buscado = Integer.parseInt(txtEventoSeleccionado.getText());
        Fiesta fiestas = new Fiesta ();
        Matrimonio matrimonio = new Matrimonio();
        for(PaqueteEvento pqEvent: col.listarPaqueteEvento()) {
            if (pqEvent.getIdEvento()== (id_evento_Buscado)) {
                if(pqEvent.getTipoEvento().equals("matrimonio")){
                    isMatrimonio = true;

                    matrimonio = new Matrimonio(txtTiempoCantPlatos.getText(), Boolean.parseBoolean(txtMusicaEnVivo.getText()), pqEvent.getIdEvento(), pqEvent.getTipoEvento(), pqEvent.getDescripcionEvento(), pqEvent.getCantidadAsistentes(), pqEvent.getTamanioRecinto(), pqEvent.getCantidadMesas(), pqEvent.getPrecio());
                }else{
                    fiestas = new Fiesta (Boolean.parseBoolean(txtConOsinAlcohol.getText()), Integer.parseInt(txtNroDjs.getText()), pqEvent.getIdEvento(), pqEvent.getTipoEvento(), pqEvent.getDescripcionEvento(), pqEvent.getCantidadAsistentes(), pqEvent.getTamanioRecinto(), pqEvent.getCantidadMesas(), pqEvent.getPrecio());
                }
            }   
        }

        Evento evento = new Evento(cliente, isMatrimonio ? matrimonio : fiestas, txtFechaHora.getText());
        col.agregarEvento(evento, jTextArea4);
    }//GEN-LAST:event_jButtonAgregarEventoSolicitadoActionPerformed

